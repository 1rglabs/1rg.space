		<p>To make sure that everyone has a common understanding of “show respect and courtesy to each other,” we have adopted the following code of conduct.</p>
		
		<h2>Unacceptable behavior</h2>
		<p>
			The following types of behavior are unacceptable at 1RG, both online and in-person, and constitute code of conduct violations.
		</p>
			
			<h3>Abusive behavior</h3>
			<p>
				<span>Harassment</span> - including offensive verbal comments related to gender, sexual orientation, disability, physical appearance, 
				body size, race, or religion, as well as sexual images in public spaces, deliberate intimidation, stalking, following, 
				harassing photography or recording, inappropriate physical contact, and unwelcome sexual or romantic attention.
			</p>
			<p>
				<span>Threats</span> — threatening someone physically or verbally. For example, threatening to publicize sensitive information about someone’s personal life.
			</p>
			
			<h3>Unwelcoming behavior</h3>
			<p><span>Blatant -isms</span> — saying things that are explicitly racist, sexist, homophobic, etc. For example, arguing that some people are less intelligent because 
				of their gender, race or religion.
			</p>

			<p>Subtle -isms are subtle expressions of racism, sexism, ageism, homophobia, transphobia and other kinds of bias and prejudice. 
				They are small things that make others feel unwelcome, things that we all sometimes do by mistake. 
				Subtle -isms make people feel like they don’t belong at 1RG.
				Subtle - isms and small mistakes made in conversation are not code of conduct violations. 
				However, repeating something after it has been pointed out to you as a subtle-ism, or antagonizing or arguing with someone who has pointed 
				out your subtle -ism is considered unwelcoming behavior, and is not allowed at 1RG.
			</p>

			<p>This distinction between subtle and blatant -isms is inspired by the 
				<a href="https://www.recurse.com/social-rules#no-subtle-isms" target="_blank">Recurse Center Social Rules</a>
			</p>

			<p><span>Being especially unpleasant</span> — for example, if we’ve received reports from multiple members of the 
				community of annoying, rude, or especially distracting behavior.
			</p>

		<h2>Scope</h2>
		<p>
			Community members are held to the standards outlined in this code of conduct when interacting in the physical 1RG space, at 1RG-run events, 
			and online on 1RG’s internal tools like community.1rg.club.
		</p>
		<p>
			The 1RG staff will use our discretion when deciding whether to enforce this code of conduct and potentially remove someone 
			from the 1RG community after reports of such behavior happening outside of 1RG, taking into account the impact on the individual 
			community members involved as well as the impact on the community at large.
		</p>

		<p>
			When in doubt, please report unacceptable behavior to us. If someone’s behavior outside of 1RG makes you feel unsafe at 1RG, 
			that is absolutely relevant and actionable for us.
		</p>

		<h2>Enforcement</h2>

		<p>We’ve categorized unacceptable behavior into abuse, unwelcoming behavior in the section above.</p>

		<p>
			If we witness or receive a report about abusive behavior, we will contact the perpetrator to have a conversation with them and verify what has transpired, 
			and they will be removed from the 1RG community.
		</p>
			
		<p>
			If we witness or receive a report about unwelcoming behavior, we will contact the individual involved to explain why their behavior was unacceptable, 
			and warn them that a second code of conduct violation will result in us removing them from the 1RG community.
		</p>

		<h2>Reporting</h2>

		<p>If you see a violation of our code of conduct, please report it to the 1RC staff.</p>

		<h3>Why should I report</h3>

		<p>You are responsible for making 1RG a safe and comfortable space for everyone. Everyone in our community shares this responsibility.</p>

		<p>
			1RG only works as a community because of shared trust between community members. 
			Reporting code of conduct violations helps us identify when this trust is broken, to prevent that from happening in the future.
		</p>

		<h3>Where and how to report</h3>
		<p>
			Please report all code of conduct violations using our reporting form. 
			If you would rather discuss the matter in person with a 1RG staff, email us to schedule a time to talk. 
			You can contact Max Veytsman at max@1rg.club and Serena Peruzzo at serena@1rg.club.
		</p>
		<p>In your report, please include:</p>
			
			<ul class="list-disc list-inside ml-4">
				<li>Your name — this is incredibly helpful for us to be able to follow up with you, and ask questions to better understand the situation. The form allows you to report anonymously. Please only use this option if you really need to, and know that we might not be able to take action without knowing who you are. In any case, provide an email address so we can correspond with you about the report. The third-party reporting tool we’re using anonymizes communication, so we never see the email address you provide if you chose to submit anonymously.</li>
				<li>A detailed description of what happened - If the violation happened online, please link to or send us the relevant text. <br>
					If the violation happened in person, please detail what exactly the other person said or did. In order to take action, we need to know the concrete actions that someone took.
				</li>
				<li>Where and when the incident happened -
					Any other relevant context. Do you have examples of a pattern of similar behavior from this person before? Do you have a relationship with this person outside of 1RG?
					If/how you’ve already responded—this lets us know the current state of the situation.
				</li>
			</ul>
		
			<h2>Confidentiality</h2>
		<p>
			We will keep all reports confidential, except if we’ve discussed with you and agreed otherwise. 
			When we discuss incidents with people who are reported, we will anonymize details as much as we can to protect reporter privacy.<br>
			However, some incidents happen in one-on-one interactions, and even if the details are anonymized, the reported person may be able to guess who made the report. 
			If you have concerns about retaliation or your personal safety, and do not want us to share the details of your report with anyone 
			(including the perpetrator) please let us know explicitly in your report. 
			Unfortunately, in that situation we will not be able to take any action.
		</p>

		<p>
			In some cases we may decide to share an update about a major incident with the 1RG community. 
			If that’s the case, the identities of all victims and reporters will remain confidential unless those individuals instruct us otherwise.
		</p>
		<p>If you have any questions about any part of the code of conduct, please reach out to any 1RG staff member.</p>

